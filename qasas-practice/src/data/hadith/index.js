function distractorsFor(record, records) {
  return records
    .filter((candidate) => candidate.id !== record.id)
    .sort((a, b) => Math.abs(a.hadithNumber - record.hadithNumber) - Math.abs(b.hadithNumber - record.hadithNumber))
    .slice(0, 3)
    .map((candidate) => candidate.correctTranslation);
}

export const hadithQuestions = hadithRecords.map((record) => ({
  ...record,
  arabicText: cleanArabicById[record.id] || record.arabicText,
  type: 'mcq',
  prompt: 'Choose the correct English translation for this Hadith.',
  options: [record.correctTranslation, ...distractorsFor(record, hadithRecords)],
  answerIndex: 0,
  explanation: record.correctTranslation,
}));

const TOPIC_BANKS = {
  ARB40: hadithQuestions,
};

export function getHadithQuestions(topicCode = 'all') {
  if (topicCode === 'all') return hadithQuestions;
  return TOPIC_BANKS[topicCode] || [];
}
